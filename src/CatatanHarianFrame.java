    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
    JFileChooser fileChooser = new JFileChooser();
    int option = fileChooser.showOpenDialog(this);

    if (option == JFileChooser.APPROVE_OPTION) {
        File file = fileChooser.getSelectedFile();

        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            StringBuilder isiCatatan = new StringBuilder();
            String judul = null;

            // Pastikan JList menggunakan DefaultListModel
            if (listCatatan.getModel() instanceof DefaultListModel) {
                DefaultListModel<String> model = (DefaultListModel<String>) listCatatan.getModel();

                // Membaca file baris demi baris
                while ((line = reader.readLine()) != null) {
                    if (line.startsWith("Judul Catatan: ")) {
                        judul = line.substring(15); // Ambil judul catatan
                    } else if (line.startsWith("Isi Catatan:")) {
                        isiCatatan.setLength(0); // Kosongkan buffer isiCatatan untuk catatan baru
                    } else {
                        isiCatatan.append(line).append("\n"); // Tambahkan baris ke isiCatatan
                    }
                }

                // Tambahkan judul catatan ke JList, bahkan jika sudah ada sebelumnya
                if (judul != null && !judul.isEmpty()) {
                    model.addElement(judul); // Tambahkan judul ke JList
                    textAreaCatatan.setText(isiCatatan.toString()); // Tampilkan isi catatan di JTextArea
                    JOptionPane.showMessageDialog(this, "Catatan berhasil diimpor!");
                } else {
                    JOptionPane.showMessageDialog(this, "File tidak memiliki judul catatan yang valid!");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Model JList bukan DefaultListModel!");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error membaca file: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_btnImportActionPerformed

    
