    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
      String judul = JOptionPane.showInputDialog("Masukkan Judul Catatan:");
    String isi = textAreaCatatan.getText();
    if (judul != null && !judul.isEmpty()) {
        Catatan baru = new Catatan(judul, isi);
        manager.tambahCatatan(baru);
        listCatatan.setListData(manager.getCatatanList().stream().map(Catatan::getJudul).toArray(String[]::new)); // Perubahan di sini
    }
    }//GEN-LAST:event_btnTambahActionPerformed

