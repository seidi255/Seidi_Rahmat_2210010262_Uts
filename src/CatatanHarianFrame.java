    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
      int selectedIndex = listCatatan.getSelectedIndex();
    if (selectedIndex != -1) {
        // Mengambil judul catatan yang dipilih dari JList
        String judul = listCatatan.getModel().getElementAt(selectedIndex);
        
        // Membersihkan karakter yang tidak valid untuk nama file
        String namaFile = judul.replaceAll("[^a-zA-Z0-9\\s]", "").replace(" ", "_");
        
        // Dialog untuk memilih lokasi file
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setSelectedFile(new File(namaFile + ".txt")); // Set judul catatan sebagai nama file default
        int option = fileChooser.showSaveDialog(this);
        
        if (option == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            try (PrintWriter writer = new PrintWriter(new FileWriter(file))) {
                // Menyimpan judul dan isi catatan ke file teks
                writer.println("Judul Catatan: " + judul);
                writer.println("\nIsi Catatan:");
                writer.println(textAreaCatatan.getText());

                JOptionPane.showMessageDialog(this, "Catatan berhasil disimpan sebagai file TXT!");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error menyimpan file: " + ex.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih catatan terlebih dahulu!");
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

